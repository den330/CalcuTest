import Foundation

class Calculator{
    
    
    func add(num1: Int, num2: Int) -> Int{
        return num1 + num2
    }
    
    func minus(num1: Int, num2: Int) -> Int{
        return num1 - num2
    }
    
    
    func multi(num1: Int, num2: Int) -> Int{
        return num1 * num2
    }
    
    func divi(num1: Int, num2: Int) -> Int{
        return num1 / num2
    }

}
